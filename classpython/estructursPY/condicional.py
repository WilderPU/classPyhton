# los condicionales son muy cheveres jajaja.
#Estos son los encargados de la toma de deciciones, pueden ser simples o multiples

# Condicional simple
color = "Red"
if color == "Red":
    print("El color es Red")
else:
    print("El color no es Red")

# Condicional multiple
tabla=int(input("Ingrese la tabla que desee"))
if tabla == 2:
    print("Se ejecuta la funcion de la tabla del 2")
elif tabla == 3:
    print("Se ejecuta la funcion de la tabla del 3")
elif tabla == 4:
    print("Se ejecuta la funcion de la tabla del 4")
elif tabla == 5:
    print("Se ejecuta la funcion de la tabla del 5")
else:
    print("No podemos hacer otra tabla")